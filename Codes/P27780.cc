#include <bits/stdc++.h>
using namespace std;

void solve(int n) {
  for(int i = 0; i < n; ++i) {
    char c;
    cin >> c;
    if(c == 'A') {
      int l, r, y1, y2;
      cin >> l >> r >> y1 >> y2;
    }
    else {
      int x;
      cin >> x;
    }
  }
}

int main() {
  int n;
  while(cin >> n) solve(n);
}
